# Introduction
# This document gives coding conventions for the Python code comprising
# the standard library in the main Python distribution. Please see the
# companion information PEP describing style guidelines for the C code
# in the C implementation of Python.

# This document and PEP 257 (Docstring Conventions) were adapted from
# Guido's original Python Style Guide essay, with some additional from
# Barry's style guide.

# This style guide evolves over time as additional conventions are identified
# and past conventions are rendered absolete by changes in the language itself.

# Many projects have their own coding style guidelines. In the event of
# any conflicts, such project-specific guides take precedence for taht project.

