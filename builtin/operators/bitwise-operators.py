# Bitwise Operators

# Python provides the following bitwise operators for integers:

    # ~     bitwise complement (prefix unary operator)
    # &     bitwise and
    # |     bitwise or
    # ^     bitwise exclusive-or
    # <<    shift bits left, filling in with zeros
    # >>    shift bits right, filling in with sign bit

print(~ 1)
print(1 & 1)
print(1 | 0)
print(1 ^ 1)
print(1 << 2)
print(4 >> 2)